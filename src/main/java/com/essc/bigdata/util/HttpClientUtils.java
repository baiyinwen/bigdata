    /**
     * 使用 HttpURLConnection 解决中文乱码导致的连接无法发送的问题
     * @param url url连接
     * @param headers header头
     * @param params body参数
     * @return 请求返回状态
     */
    public static String doPostNew(String url, Map<String, String> headers, Map<String, String> params) {
        HttpURLConnection connection = null;
        InputStream is = null;
        OutputStream os = null;
        BufferedReader br = null;
        String result = null;
        try {
            // 通过远程url连接对象打开连接
            URL url1 = new URL(url);
            connection = (HttpURLConnection) url1.openConnection();
            // 设置连接请求方式
            connection.setRequestMethod("POST");
            // 设置连接主机服务器超时时间：15000毫秒
            connection.setConnectTimeout(15000);
            // 设置读取主机服务器返回数据超时时间：60000毫秒
            connection.setReadTimeout(60000);
            // 默认值为：false，当向远程服务器传送数据/写数据时，需要设置为true
            connection.setDoOutput(true);
            // 默认值为：true，当前向远程服务读取数据时，设置为true，该参数可有可无
            connection.setDoInput(true);
            // 设置传入参数的格式:请求参数应该是 name1=value1&name2=value2 的形式。
            connection.setRequestProperty(Constants.MESSAGE_CONTENT_TYPE, headers.get(Constants.MESSAGE_CONTENT_TYPE));
            connection.setRequestProperty(Constants.MESSAGE_APPID, headers.get(Constants.MESSAGE_APPID));
            connection.setRequestProperty(Constants.MESSAGE_SIGNINFO, headers.get(Constants.MESSAGE_SIGNINFO));

            JSONObject param = new JSONObject();
            param.putAll(params);
            // 通过连接对象获取一个输出流
            os = connection.getOutputStream();
            // 通过输出流对象将参数写出去/传输出去,它是通过字节数组写出的
            os.write(param.toJSONString().getBytes());

//            // 得到请求的输出流对象
//            OutputStreamWriter writer = new OutputStreamWriter(connection.getOutputStream(),"UTF-8");
//            writer.write(param.toString());
//            writer.flush();

            // 通过连接对象获取一个输入流，向远程读取
            if (connection.getResponseCode() == 200) {
                is = connection.getInputStream();
                // 对输入流对象进行包装:charset根据工作项目组的要求来设置
                br = new BufferedReader(new InputStreamReader(is, "UTF-8"));
                StringBuffer sbf = new StringBuffer();
                String temp = null;
                // 循环遍历一行一行读取数据
                while ((temp = br.readLine()) != null) {
                    sbf.append(temp);
                    sbf.append("\r\n");
                }
                result = sbf.toString();
            }
        } catch (IOException e) {
            throw new RuntimeException(e);
        } finally {
            // 关闭资源
            if (null != br) {
                try {
                    br.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
            if (null != os) {
                try {
                    os.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
            if (null != is) {
                try {
                    is.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
            // 断开与远程地址url的连接
            connection.disconnect();
        }
        return result;
    }

